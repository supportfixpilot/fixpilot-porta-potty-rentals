        ("Atlanta Porta Potty Rentals", city_data["og_title"].replace(" | Same-Day Delivery", "")),
        (f'<meta property="og:description" content="From Mercedes-Benz Stadium to Buckhead weddings', f'<meta property="og:description" content="{city_data["og_description"]}'),
        ("Atlanta Sanitation Solutions", f"{city_data['name']} Sanitation Solutions"),
        ("100 Peachtree St NW", city_data["address_street"]),
        ('"Atlanta"', f'"{city_data["name"]}"'),
        ('"30303"', f'"{city_data["address_zip"]}"'),
        ("33.7537;-84..99", f"0;0"),
        (f'"{city_data["theme_color"]}', f'"{city_data["theme_color"]}'),
        ("Mercedes-Benz Stadium, Braves games", city_data["services_keywords"]),
        ("Peach State", city_data["hero_keyword"]),
        ("Buckhead", city_data["areas"][0][0]),
        ("Peachtree Corners", city_data["areas"][1][0]),
        ("Alpharetta", city_data["areas"][2][0]),
