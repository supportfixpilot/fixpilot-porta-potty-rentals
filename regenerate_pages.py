def main():
    if not TEMPLATE_PATH.exists():
        print(f"Template not found: {TEMPLATE_PATH}")
        return
    
    fixed = 0
    for city_slug, city_data in CITY_DATA.items():
        print(f"Regenerating {city_slug}...")
        html = regenerate_city_page(city_slug, city_data)
        
        output_path = Path(f"porta-potty-rental-{city_slug}/index.html")
        if output_path.exists():
            with open(output_path, "w", encoding="utf-8") as f:
                f.write(html)
            print(f"  Done: {city_slug}")
            fixed += 1
        else:
            print(f"  Skip: {city_slug} (not found)")
    
    print(f"\nTotal regenerated: {fixed}")

if __name__ == "__main__":
    main()
